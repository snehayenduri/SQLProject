    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblRestaurant.getModel();
        dtm.setRowCount(0);
        for(Table t : tableDirectory.getTableDirectory()){
            Object[] row = new Object[3];
            row[0] = t;
            row[1] = t.getTableStatus();
            row[2] = t.getSeatingCapacity();
            
            dtm.addRow(row);
        }
       
    }
