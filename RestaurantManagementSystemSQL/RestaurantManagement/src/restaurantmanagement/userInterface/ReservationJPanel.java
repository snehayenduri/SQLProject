    private void btnReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReserveActionPerformed
        // TODO add your handling code here:
         DefaultTableModel acctTbl = (DefaultTableModel) tblRestaurant.getModel();
        int selectedRow = tblRestaurant.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(this, "Please select a table");
            return;
        }
        Table table = (Table) acctTbl.getValueAt(selectedRow,0);
        try {
            conn.reserveTable(table);
            JOptionPane.showMessageDialog(this, "Reserved table "+table.getTableNumber()+" for you");
        } catch (SQLException ex) {
            Logger.getLogger(ReservationJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnReserveActionPerformed

